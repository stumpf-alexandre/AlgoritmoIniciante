public class While {
    public static void main(String[] args) {
        int cont = 0;
        while (cont < 20){
            System.out.println("Contador: " + cont);
            cont++;
        }

        int contInverso = 100;
        while (contInverso > 20){
            System.out.println("Contador Inverso: " +contInverso);
            contInverso--;
        }
    }
}